        <h1 class="section-title">About the Band</h1>
          <p class="section-paragraph">Alice in Chains is an American grunge/ alternative metal band from Seattle, Washington, formed in 1987 by guitarist/vocalist Jerry Cantrell and drummer Sean Kinney, who then recruited bassist Mike Starr and lead vocalist Layne Staley. Starr was replaced by Mike Inez in 1993. William DuVall joined the band in 2006 as co-lead vocalist and rhythm guitarist, replacing Staley, who died in 2002. The band took its name from Staley's previous group, the glam metal band Alice N' Chains.</p>
          <p class="section-paragraph">Although widely associated with grunge music, the band's sound incorporates heavy metal elements. Since its formation, Alice in Chains has released five studio albums, three EPs, three live albums, four compilations, two DVDs, 31 music videos and 29 singles. The band is known for its distinctive vocal style, which often included the harmonized vocals between Staley and Cantrell (and later between Cantrell and William DuVall). Cantrell started to sing lead vocals on the 1992 acoustic EP Sap, and his role continued to grow in the following albums, making Alice in Chains a two-vocal band.</p>
          <p class="section-paragraph">Alice in Chains rose to international fame as part of the grunge movement of the early 1990s, along with other Seattle bands such as Nirvana, Pearl Jam, and Soundgarden. The band was one of the most successful music acts of the 1990s, selling over 20 million records worldwide, and over 14 million records in the US alone, with two No. 1 albums and six Top 10 albums on the Billboard 200 chart. The band has had 16 Top 10 songs on Billboard's Mainstream Rock Tracks chart, 5 No. 1 hits, and nine Grammy Award nominations. Their debut album, Facelift, featuring the hit single "Man In The Box", was released in 1990 and has been certified double-platinum by the RIAA, selling over two million copies. In 1992, the band's second album, Dirt, was released to critical acclaim and was certified quadruple platinum. Their second acoustic EP, Jar of Flies, debuted at No. 1 on the Billboard 200 chart in 1994, becoming the first ever EP and first Alice in Chains release to top the charts, and it has been certified triple platinum by the RIAA. The band's third album, Alice in Chains debuted at No. 1 on the Billboard 200 in 1995 and has been certified double platinum.</p>
          <p class="section-paragraph">Although never officially disbanding, Alice in Chains was plagued by extended inactivity from 1996 onwards due to Staley's substance abuse, which resulted in his death in 2002. The band reunited in 2005 for a live benefit show, performing with a number of guest vocalists. They toured in 2006, with William DuVall taking over as lead vocalist full-time. The new line-up released the band's fourth studio album, Black Gives Way to Blue, in 2009, which received gold certification by the RIAA and two Grammy nominations. Their fifth studio album, The Devil Put Dinosaurs Here, was released in 2013 and debuted at No. 2 on the Billboard 200. The band toured extensively and released several videos in support of these albums.</p>
          <p class="section-paragraph">Their sixth studio album, Rainier Fog, would be released in 2018, through BMG records, making it their first big release on the new label. Rainier Fog would end up debuting at No. 12 on the Billboard 200, making it yet another powerful addition to the band's already impressive discography.</p>
          <p class="section-paragraph">Although Alice in Chains has been labeled grunge by the mainstream media, Jerry Cantrell identifies the band as primarily heavy metal. He told Guitar World in 1996, "We're a lot of different things … I don't quite know what the mixture is, but there's definitely metal, blues, rock and roll, maybe a touch of punk. The metal part will never leave, and I never want it to". The Edmonton Journal has stated, "Living and playing in Seattle might have got them the grunge tag, but they've always pretty much been a classic metal band to the core."</p>
          <p class="section-paragraph">Over the course of their career, the band's sound has also been described as alternative metal, sludge metal, doom metal,drone rock, hard rock, and alternative rock. Regarding the band's constant categorization by the media, Cantrell stated "When we first came out we were metal. Then we started being called alternative metal. Then grunge came out and then we were hard rock. And now, since we've started doing this again I've seen us listed as: hard rock, alternative, alternative metal and just straight metal. I walked into an HMV the other day to check out the placement and see what's on and they've got us relegated back into the metal section. Right back where we started!". Drummer Sean Kinney rejects the grunge label, stating in a 2013 interview "I mean, before we first came out there was no grunge, they hadn’t invented that word. Before they invented the word grunge we were alternative rock and alternative metal and metal and rock, and we didn’t give a shit whatever, we were a rock and roll band!". According to Mike Inez, they were always the metal stepchildren of the Seattle scene.</p>
          <p class="section-paragraph">Jerry Cantrell's guitar style combines "pummeling riffs and expansive guitar textures" to create "slow, brooding minor-key grinds". He is also recognized for his natural ability to blend acoustic and electric guitars. While down-tuned, distorted guitars mixed with Staley's distinctive "snarl-to-a-scream" vocals appealed to heavy metal fans, the band also had "a sense of melody that was undeniable", which introduced Alice in Chains to a much wider audience outside of the heavy metal underground.</p>
          <p class="section-paragraph">According to Stephen Thomas Erlewine of AllMusic, Alice in Chains' sound has a "Black Sabbath-style riffing and an unconventional vocal style". The band has been described by Erlewine as "hard enough for metal fans, yet their dark subject matter and punky attack placed them among the front ranks of the Seattle-based grunge bands". Three of the band's releases feature acoustic music, and while the band initially kept these releases separate, Alice in Chains' self-titled album combined the styles to form "a bleak, nihilistic sound that balanced grinding hard rock with subtly textured acoustic numbers".</p>
          <p class="section-paragraph">Alice in Chains is also noted for the unique vocal harmonies of Staley (or DuVall) and Cantrell, which included overlapping passages, dual lead vocals, and trademark harmonies typically separated by a major third. Cantrell said it was Staley who gave him the self-assurance to sing his own songs. Alyssa Burrows said the band's distinctive sound "came from Staley's vocal style and his lyrics dealing with personal struggles and addiction". Staley's songs were often considered "dark", with themes such as drug abuse, depression, and suicide, while Cantrell's lyrics often dealt with personal relationships.</p>
